// The DOM is a programming interface for HTML and XML documents.
// It represents the document as a tree structure where each node is an object representing a part of the document.
// The DOM allows programming languages to manipulate the structure, style, and content of documents.   
// The DOM is a standard that defines the structure of documents and how to access and manipulate them.
// The DOM (Document Object Model) is a programming interface for web documents. 

// example of DOM Manipulation

const button = document.getElementById("btn");
button.addEventListener("click", () => {
  alert("Button clicked!");
});
// This code selects a button with the ID "btn" and adds an event listener to it.
// When the button is clicked, an alert box will appear with the message "Button clicked!". 
// This is a simple example of how to manipulate the DOM using JavaScript.

